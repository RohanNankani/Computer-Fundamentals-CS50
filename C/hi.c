#include <stdio.h>

int main()
{
    char c1 = 'H';
    char c2 = 'I';
    char c3 = '!';

    printf("%i %i %i\n", c1, c2, c3); // To get the number of these character in the ASCII table, you can type ("%i %i %i") instead of ("%c %c %c"), to tell the computer to print the number instead of the character.
}

// When using char, you need to use single quotation to tell the computer it is a char character ('H').